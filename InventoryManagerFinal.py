def read_from_csv(existing_inventory, file_name='inventory.csv'):
    """ reads existing inventory info from csv file
        into an Inventory obj """

    print('Reading from csv file for existing inventory...')
    csv = open(file_name, 'r')
    for line in csv:
        item = line.rstrip().split(',,')
        if len(item) > 2:
            item[1] = item[1].rstrip('L').rstrip('m')
            item[3] = item[3].lstrip('$')
            existing_inventory.add(item[0], item[1], int(item[2]), float(item[3]))

    csv.close()


