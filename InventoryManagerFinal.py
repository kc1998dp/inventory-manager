class Inventory:
    """ inventory type, represents a collection of Liquor objs
        supports basic display functions
        supports adding, removing, price changing """
    def __init__(self):
        """ _stock is a dict containing strings mapped to Liquor objs
            _bts is an int tracking the total number of bottles
            _value is a float tracking the total value of the all inventory """
        self._stock = {}
        self._bts = 0
        self._value = 0

    def __len__(self):
        """ returns number of bottles"""
        return self._bts

    def __getitem__(self, item):
        """ returns the datas associated with the item """
        if item in self._stock:
            return self._stock[item]
        else:
            print('Item not found in inventory.')
            return None

    def __iter__(self):
        """ generator that yields items (liquor objs) in stock """
        for item in self._stock:
            yield self._stock[item]

    def add(self, item_name, size, amt, price):
        """ adds new item to stock """
        size = size.rstrip('L').rstrip('m')
        if item_name not in self._stock:
            new_item = Liquor(item_name)
            new_item.add_size(size, amt, price)
            self._stock[item_name] = new_item
        else:
            if size in self._stock[item_name]:
                if self._stock[item_name].get_price(size) != price:
                    self._stock[item_name].change_price(size, price)
                self._stock[item_name].add(size, amt)
            else:
                self._stock[item_name].add_size(size, amt, price)

        self._bts += amt
        self._value += amt * price

    def remove(self, item_name, size, amt):
        """ removes an item from stock """
        size = size.rstrip('L').rstrip('m')
        if self._stock[item_name].get_amt(size) - amt >= 0:
            self._stock[item_name].remove(size, amt)
            self._bts -= amt
            self._value -= amt * self._stock[item_name].get_price(size)
        else:
            print("Can't remove", amt, "of stock because", amt, \
                  "exceeds amount of stock, which is", self._sizes[size][0], \
                  "bottles")

    def change_price(self, item_name, size, new_price):
        """ changes price of an item """
        size = size.rstrip('L').rstrip('m')
        self._value -= self._stock[item_name].get_value(size)
        self._stock[item_name].change_price(size, new_price)
        self._value += self._stock[item_name].get_value(size)

    def get_bts(self):
        return self._bts

    def get_value(self):
        return self._value

    def display(self):
        """ prints out all items in stock """
        for liquor in self._stock:
            print(self._stock[liquor])
        print('\n', 'GRAND TOTAL: $ ', self._value, sep='')


class Liquor:
    def __init__(self, name):
        """ _name contains a string
            _sizes is a dict containing strings
            (representing sizes) mapped to lists """
        self._name = name
        self._sizes = {}

    def __getitem__(self, size):
        """ returns datas associated with a size
            ind 0 refers to amt
            ind 1 refers to price
            ind 2 refers to value """
        if size in self._sizes:
            return self._sizes[size]
        else:
            print("Size not found.")
            return None

    def __iter__(self):
        """ generator that yields SIZEs (keys) of item
            NOT the associated datas (values) """
        for size in self._sizes:
            yield size

    def add_size(self, size, amt, price):
        """ add a new size of this item to the inventory
            ind 0 refers to amt
            ind 1 refers to price
            ind 2 refers to value """
        if size not in self._sizes:
            self._sizes[size] = [amt, price, amt * price]
        else:
            print('Size already exists in inventory.')

    def add(self, size, amt):
        """ ind 0 refers to amt
            ind 1 refers to price
            ind 2 refers to value """
        self._sizes[size][0] += amt
        self._sizes[size][2] += amt * self._sizes[size][1]

    def change_price(self, size, new_price):
        """ change the price of a size of this item
            modifies value to reflect change in price
            ind 0 refers to amt
            ind 1 refers to price
            ind 2 refers to value """

        if self._sizes[size][1] != new_price:
            self._sizes[size][1] = new_price
            self._sizes[size][2] = self._sizes[size][0] * new_price

    def remove(self, size, amt):
        """ remove an amt of stock of a particular size
            modify value to reflect changes
            ind 0 refers to amt
            ind 1 refers to price
            ind 2 refers to value """

        if self._sizes[size][0] - amt >= 0:
            self._sizes[size][0] -= amt
            self._sizes[size][2] = self._sizes[size][0] * self._sizes[size][1]

    def get_name(self):
        return self._name

    def get_amt(self, size):
        return self._sizes[size][0]

    def get_price(self, size):
        return self._sizes[size][1]

    def get_value(self, size):
        return self._sizes[size][2]

    def __repr__(self):
        output = 'Item Name: ' + self._name + '\n\t'
        total_value = 0
        for size in self._sizes:
            float_size = float(size)
            if float_size < 50:
                float_size = str(size) + 'L'
            else:
                float_size = str(size) + 'mL'
            output += 'Size: ' + str(float_size) + '\n\t\t' + 'Amt: ' + \
                       str(self._sizes[size][0]) + '\n\t\t' + 'Price: $' + \
                       str(self._sizes[size][1]) + '\n\t\t' + 'Value: $' + \
                       str(self._sizes[size][2]) + '\n'
            total_value += self._sizes[size][2]
        output += '\n' + 'Total Value: $' + str(total_value) + '\n------------------'
        return output


